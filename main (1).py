print("Calculate the Area of a Trapezoid")
Base_A = int(input("Enter Base A in centimeters:"))
Base_B = int(input("Enter Base B in centimeters:"))
Height = int(input("Enter Height in centimeters:"))
A_B = Base_A + Base_B
A_B2 = A_B / 2
Final = A_B2 * Height
print("The area of the Trapezoid is:")
print(Final)